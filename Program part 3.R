df <- tibble(
  a = rnorm(10),
  b = rnorm(10),
  c = rnorm(10),
  d = rnorm(10)
)
rescale01 <- function(x) {
  rng <- range(x, na.rm = TRUE)
  (x - rng[1]) / (rng[2] - rng[1])
}

df$a <- rescale01(df$a)
df$b <- rescale01(df$b)
df$c <- rescale01(df$c)
df$d <- rescale01(df$d)

## Dette kan bli erstattet av fÃ¸lgende loop

for (i in seq_along(df)) {
  df[[i]] <- rescale01(df[[i]])
}

flip <- function() sample(c("T", "H"), 1)

